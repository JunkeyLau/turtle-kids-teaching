# 在for循环中执行更多操作 短信群发 30mins
# 使用for循环为每位小朋友打印“你是我的好朋友！”
friends = ["俸诚希", "唐瀚宇", "吴泓怿", "贾汶瀚", "常博康", "李子见", "马凌宵", "刘奕辰"]
for name in friends:
    print(name + "，你是我的好朋友！")
    print(name + "我们一起好好学习Python吧！\n")
# 缩进与for循环的结束
print("你们每一个人都是我一起学习Python的伙伴！")

