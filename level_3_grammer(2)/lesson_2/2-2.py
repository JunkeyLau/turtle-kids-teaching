# 月饼字典
# 月饼的口味有：蛋黄莲蓉，五仁，椒盐，叉烧，火腿
# 创建五个月饼的字典，每个字典包含月饼的馅料，甜还是咸，用列表封装起来
moon_cakes = [{"馅料":"蛋黄莲蓉", "口味":"甜"},{"馅料":"五仁", "口味":"甜"},
{"馅料":"椒盐", "口味":"咸"},{"馅料":"叉烧", "口味":"甜咸"},{"馅料":"火腿", "口味":"甜咸"}]

for n in range(5):
    print("第" + str(n+1) + "个月饼的馅料是：" + moon_cakes[n]["馅料"] + "，口味：" + moon_cakes[n]["口味"])