# 本节课教学如何绘制小猪佩奇的家(暂时不画太阳和草地)
from turtle import *
screensize(600, 600)
speed(1) # 设置画笔速度
goto(0,100)
# 绘制左边的部分
color("grey","yellow")
begin_fill()
right(90)
forward(400)
left(90)
forward(20)
left(90)
forward(160)
right(90)
forward(40)
right(90)
forward(160)
left(90)
forward(20)
left(90)
forward(400)
left(90)
forward(80)
end_fill()
penup()
backward(20)
left(90)
forward(20)
# 绘制左边的窗户
pendown()
color("white","blue")
pensize(5)
begin_fill()
forward(80)
left(90)
forward(20)
left(90)
forward(80)
end_fill()
begin_fill()
right(90)
forward(20)
right(90)
forward(80)
right(90)
forward(20)
end_fill()
penup()
backward(20)
left(90)
backward(40)
pendown()
right(90)
forward(40)

done()