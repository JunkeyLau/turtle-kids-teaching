from turtle import *
screensize(300,300)
penup()
goto(-100,50)
pendown()
color("red","red") # 画外面的红色框框
begin_fill()
forward(200)
right(90)
forward(100)
right(90)
forward(200)
right(90)
forward(100)
right(90)
end_fill()
fillcolor("white") # 开始画中间的三角形
penup()
goto(-12.5, 25)
pendown()
begin_fill()
right(30)
forward(50)
right(120)
forward(50)
right(120)
forward(50)
end_fill()
done()